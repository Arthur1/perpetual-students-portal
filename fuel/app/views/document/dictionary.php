<h2 class="green-text text-darken-1">全混ぜ</h2>
<p>
	全混ぜと名乗っているが、<mark>全部は混ざっていない</mark>。使用デッキはEIK・WM・FL・WA・FR・NL・BI・P・C・Oである。GデッキZデッキ売ってるところあったら教えてください。L
	デッキは入れません。Xデッキは入れるときもあれば入れないときもあり、使用する場合は「＋Xデッキ」と明記する。泥沼も同様。
</p>
<h2 class="green-text text-darken-1">EIK</h2>
<p>
	イーアイケーと読む人もいれば、アイクと読む人もいる。後者はスマブラ勢に多い。
</p>
<h2 class="green-text text-darken-1">Jデッキ</h2>
<p>
	工大祭実行委員会(以後JIZI)で制作したオリジナルデッキ。JIZIの委員や工大祭に関するものがカードになっている。
</p>
<h2 class="green-text text-darken-1">SVデッキ</h2>
<p>
	JIZIで制作したオリジナルデッキ第2弾。シャドウバースをモチーフに作ったが、<s>原作譲りの</s>あまりのカードバランスの悪さにプレイされることはほぼない。
</p>
<h2 class="green-text text-darken-1">ぶらつき学生連盟</h2>
<p>
	本サークルの名前。命名理由は、JIZIからの部員のうち、7割以上が留年を経験したからである。東工大のサークルだからといって「～～てっく」にするのはつまらないので却下。
</p>
