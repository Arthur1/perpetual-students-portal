<h1 class="orange-text text-darken-1">ハウスルール</h1>
ここでは、「ぶらつき学生連盟」で主に採用されているハウスルールを紹介する。一般的なルールと異なるものも多い。全体的に、Unofficial Agricola Compendiumなどの非公式裁定やエラッタは導入されていないことが多い。
<h2 class="green-text text-darken-1">手番の決め方</h2>
<p>
	2かまど・3かまど・4調理場・5調理場・<mark>レンガ暖炉</mark>を使用する。<s>井戸のカード番号は10なので10番手。</s>
</p>
<h2 class="green-text text-darken-1">5番手救済</h2>
<p>
	特になし。
</p>
<h2 class="green-text text-darken-1">スタートプレイヤー</h2>
<p>
	職業・進歩の効果などで、「SP/小さい進歩1」のアクションに2人以上入った場合、<mark>最後にスタートプレイヤーを取った人</mark>が次のラウンドのスタートプレイヤーとなる。
</p>
<h2 class="green-text text-darken-1">毒見役</h2>
<p>
	<mark>飯1起動</mark>。磔の刑に処す人もいるが。
</p>
<h2 class="green-text text-darken-1">葦の家の住人</h2>
<p>
	<mark>葦の家の住人は家族ではない</mark>というのが原則。共同隊長など、家族の数を参照するすべてのカードに影響しない。ネズミ取りでも取られない。
</p>
<h2 class="green-text text-darken-1">柵立て</h2>
<p>
	15本立てられる。原語版だと14本までらしい。
</p>
<h2 class="green-text text-darken-1">木材荷車</h2>
<p>
	葦石木で起動する。普通は起動しない方が主流かも。
</p>
<h2 class="green-text text-darken-1">共同体長</h2>
<p>
	ゲストマーカー・葦の家の住人は数えない。
</p>
<h2 class="green-text text-darken-1">学者</h2>
<p>
	大きい進歩も取れる。
</p>
<h2 class="green-text text-darken-1">ウマ</h2>
<p>
	ウマによるボーナスを得たとしても、村長の対象にはならない(マイナス点があるとする)。拡張に含まれる同様のカードも同じ裁定。
</p>
<h2 class="green-text text-darken-1">愛人</h2>
<p>
	泥沼拡張時はラウンド3以降でないと出せない。
</p>
<h2 class="green-text text-darken-1">物置小屋(WM110)</h2>
<p>
	条件の柵1→厩1
</p>
<h2 class="green-text text-darken-1">世論誘導者(O06)</h2>
<p>
	面白いので特定のプレイヤーしか入れない<mark>マイナーアクションにもマーカーを置ける</mark>ようにしている。コレクターの女(NL119)と礼拝堂(C17)を全員が使うひどいゲームもあった。
</p>
<h2 class="green-text text-darken-1">シュールレアリスト(WA056)</h2>
<p>
	泥沼時、林務官舎・騎手の庭、もともとの下段大進歩の下にあるものも対象。
</p>
<h2 class="green-text text-darken-1">品物が置かれていない累積スペース</h2>
<p>
	入れない。葦石木の葦だけ取られている場合も入ることはできない。
</p>
<h2 class="green-text text-darken-1">進歩条件の「～～畑」</h2>
<p>
	基本的にはカードの効果で畑とみなすものすべてを数えることができる。
</p>
<h2 class="green-text text-darken-1">裏返したカード</h2>
<p>
	裏返したカードを再度裏返すと表になる。
</p>
<h2 class="green-text text-darken-1">その他</h2>
<p>
	他プレイヤーの同意を得られればカードの効果を自由に解釈して良い。面白さが第一。
</p>
