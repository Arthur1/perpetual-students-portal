<h2 class="green-text text-darken-1">アグリコラ拡張本って？</h2>
<p>
	アグリコラには、基本のEIKデッキ以外にも数多くの拡張デッキが発売されています。私たち「ぶらつき学生連盟」では、拡張カードを使用したアグリコラをプレイし、その結果を統計として本サイト「ぶらつき学生ポータル」に残しています。そんなデータと数多くのプレイ経験から生まれたのがこのアグリコラ拡張本。全100ページのそこそこなボリューム。500円という制作費ギリギリの頒価。拡張未経験者から経験者まで幅広くターゲットにした内容になっています。
</p>
